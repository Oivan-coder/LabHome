            secondTitle.textContent = '–í—Ç–æ—Ä–∞—è —Ç–µ–º–∞ –≤—ã—Å—Ç—É–ø–ª–µ–Ω–∏—è:';
            
            const secondText = document.createElement('div');
            secondText.className = 'modal-topic-text';
            secondText.textContent = secondSpeech.replace('–î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π –¥–æ–∫–ª–∞–¥: ', '');
            
